public class Variable {
    public static void main(String[] args) {
        String name;
        name = "Iqbal Suwandi";

        System.out.println(name);

        int age = 22;
        String address = "Indonesia";

        System.out.println(age);
        System.out.println(address);

        name = "Steven";
        System.out.println(name);
    }
}